    private void actualizarArchivoDesdeTabla() {
    int rowCount = modeloTabla.getRowCount();
    StringBuilder datos = new StringBuilder();

    for (int i = 0; i < rowCount; i++) {
        for (int j = 0; j < modeloTabla.getColumnCount(); j++) {
            datos.append(modeloTabla.getValueAt(i, j));

            if (j < modeloTabla.getColumnCount() - 1) {
                datos.append(";");
            }
        }

        if (i < rowCount - 1) {
            datos.append("\n");
        }
    }

    String rutaArchivo = "C:/Users/SDrozer/Documents/datos/";
    String nombreArchivo = "datos.txt";
    
    try (BufferedWriter escritor = new BufferedWriter(new FileWriter(rutaArchivo + nombreArchivo, false))) {
        escritor.write(datos.toString());
        System.out.println("Los datos se han actualizado en " + rutaArchivo + nombreArchivo + " exitosamente.");
    } catch (IOException e) {
        System.err.println("Error al escribir en el archivo: " + e.getMessage());
    }
}



    private void mostrarDatosEnTabla() {
    String rutaArchivo = "C:/Users/SDrozer/Documents/datos/";
    String nombreArchivo = "datos.txt";
    String datos = GuardarDatosEnArchivo.leerDatos(rutaArchivo, nombreArchivo);

    if (datos != null && !datos.isEmpty()) {
        agregarDatosATabla(datos);
    }
}

    private void limpiarTabla() {
        while (modeloTabla.getRowCount() > 0) {
            modeloTabla.removeRow(0);
        }
    }

