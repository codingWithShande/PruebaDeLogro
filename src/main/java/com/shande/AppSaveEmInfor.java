   private void agregarDatosATabla(String datos) {
    String[] lineas = datos.split("\n");

    for (String linea : lineas) {
        if (!linea.trim().isEmpty()) {
            String[] datosEmpleado = linea.split(";");

            if (datosEmpleado.length >= 8) {
                Object[] fila = new Object[8];

                for (int i = 0; i < 8; i++) {
                    fila[i] = datosEmpleado[i];
                }

                modeloTabla.addRow(fila);
            } else {
                System.err.println("No hay suficientes campos en la línea de datos del empleado: " + linea);

                System.out.println("Campos: " + Arrays.toString(datosEmpleado));
            }
        }
    }
}
